async function saveProductsToServer() {
    const password = getAdminPassword();
    if (!password) {
        throw new Error('–ü–∞—Ä–æ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω');
    }
    
    console.log('üîç ===== –ù–ê–ß–ê–õ–û saveProductsToServer =====');
    console.log('üîç –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –Ω–∞ —Å–µ—Ä–≤–µ—Ä:', Object.keys(products));
    
    // –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º —Å–∫—Ä—ã—Ç—ã–µ —Ç–æ–≤–∞—Ä—ã –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
    let hiddenCount = 0;
    let availableCount = 0;
    for (const [categoryId, categoryProducts] of Object.entries(products)) {
        for (const product of categoryProducts) {
            if (product.available === false) {
                hiddenCount++;
                console.log(`üîç –°–∫—Ä—ã—Ç—ã–π —Ç–æ–≤–∞—Ä: ${product.name} (${categoryId})`);
            } else {
                availableCount++;
            }
        }
    }
    console.log(`üîç –í—Å–µ–≥–æ —Ç–æ–≤–∞—Ä–æ–≤: ${availableCount + hiddenCount}, –¥–æ—Å—Ç—É–ø–Ω–æ: ${availableCount}, —Å–∫—Ä—ã—Ç–æ: ${hiddenCount}`);
    
    // API –∑–∞–ø—Ä–æ—Å –∫ —Å–µ—Ä–≤–µ—Ä—É
    try {
        console.log('üîç –û—Ç–ø—Ä–∞–≤–ª—è–µ–º PUT –∑–∞–ø—Ä–æ—Å –Ω–∞:', `${API_BASE}/api/admin/products`);
        const response = await fetch(`${API_BASE}/api/admin/products`, {
            method: 'PUT',
            headers: { 
                'Content-Type': 'application/json',
                'X-Admin-Password': password
            },
            body: JSON.stringify({ products })
        });
        
        console.log('üîç –û—Ç–≤–µ—Ç —Å–µ—Ä–≤–µ—Ä–∞:', response.status, response.statusText);
        
        if (!response.ok) {
            const errorText = await response.text();
            console.error('‚ùå –û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞:', errorText);
            throw new Error(`HTTP ${response.status}: ${errorText}`);
        }
        
        const result = await response.json();
        console.log('üîç –†–µ–∑—É–ª—å—Ç–∞—Ç —Å–µ—Ä–≤–µ—Ä–∞:', result);
        
        if (!result.ok) {
            throw new Error(result.error || '–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞');
        }
        
        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ localStorage –∫–∞–∫ backup
        localStorage.setItem('admin_products', JSON.stringify(products));
        console.log('‚úÖ saveProductsToServer: –£—Å–ø–µ—à–Ω–æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–æ –Ω–∞ —Å–µ—Ä–≤–µ—Ä –∏ –≤ localStorage');
        
    } catch (error) {
        console.error('‚ùå –û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –Ω–∞ —Å–µ—Ä–≤–µ—Ä:', error);
        // Fallback - —Å–æ—Ö—Ä–∞–Ω—è–µ–º –ª–æ–∫–∞–ª—å–Ω–æ
        localStorage.setItem('admin_products', JSON.stringify(products));
        console.log('‚ö†Ô∏è saveProductsToServer: Fallback - —Å–æ—Ö—Ä–∞–Ω–µ–Ω–æ —Ç–æ–ª—å–∫–æ –ª–æ–∫–∞–ª—å–Ω–æ');
        throw error;
    }
}

